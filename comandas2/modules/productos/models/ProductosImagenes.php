 * @property integer $size
 * @property integer $productos_id
 *
 * @property Productos $productos
 */
class ProductosImagenes extends \yii\db\ActiveRecord
{
    public $imageUrl;
    /**
     * @inheritdoc
     */
    public static function tableName()
    {
        return 'productos_imagenes';
    }

    /**
     * @inheritdoc
     */
    public function rules()
    {
        return [
            [['nombre', 'productos_id'], 'required'],
            [['productos_id','size'], 'integer'],
            [['nombre','path','type'], 'string', 'max' => 45],
            [['productos_id'], 'exist', 'skipOnError' => true, 'targetClass' => Productos::className(), 'targetAttribute' => ['productos_id' => 'id']],
        ];
    }

    /**
     * @inheritdoc
     */
    public function attributeLabels()
    {
        return [
            'id' => Yii::t('app', 'ID'),
            'nombre' => Yii::t('app', 'Nombre'),
            'producto_id' => Yii::t('app', 'Producto ID'),
            'productos_id' => Yii::t('app', 'Productos ID'),
        ];
    }

    public function fields()
    {
        $fields =  parent::fields(); // TODO: Change the autogenerated stub
        $fields[] = 'imageUrl';
        return $fields;
    }

    public function afterFind()
    {
        /*$this->imageUrl = EasyThumbnailImage::thumbnailFileUrl(
