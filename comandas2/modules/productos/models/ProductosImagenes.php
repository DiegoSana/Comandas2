            1000,
            1000,
            EasyThumbnailImage::THUMBNAIL_OUTBOUND
        );
        $this->imageUrl = str_replace('web/','',Url::to($this->imageUrl, true));*/
        $this->imageUrl = str_replace('web/','',Yii::$app->params['baseUrl'].'/'.$this->path . '/' . $this->nombre);
        parent::afterFind(); // TODO: Change the autogenerated stub
    }

    /**
     * @return \yii\db\ActiveQuery
     */
    public function getProductos()
    {
        return $this->hasOne(Productos::className(), ['id' => 'productos_id']);
    }

    public function setNewName() {
        switch ($this->type) {
            case 'image/gif':
                $ext = '.gif';
                break;
            case 'image/jpg':
                $ext = '.jpg';
                break;
            case 'image/jpeg':
                $ext = '.jpeg';
                break;
            case 'image/png':
                $ext = '.png';
                break;
            default:
                $ext = '.jpeg';
                break;
        }
        $this->nombre = mktime().$ext;
    }
}
